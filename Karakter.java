public class Karakter {

    int elet;
    String nev;
    int pozicio;

    public Karakter() {
    }

    public int getElet() {
        return elet;
    }

    public void setElet(int elet) {
        this.elet = elet;
    }

    public String getNev() {
        return nev;
    }

    public void setNev(String nev) {
        this.nev = nev;
    }

    public int getPozicio() {
        return pozicio;
    }

    public void setPozicio(int pozicio) {
        this.pozicio = pozicio;
    }
}
